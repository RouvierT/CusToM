function [OsteoArticularModel]= PolyThoraxSym(OsteoArticularModel,k,Mass,AttachmentPoint)
%% Implementation of a model of the spine with 5 lumbar vertebrae and 12 thoracic vertebrae
% Ce modèle inclut 13 solides correspondant aux vertèbres thoraciques
% (T1 à T12) et aux cervicales (comme un solide rigide) reliés entre eux par des 
% liaisons rotules (3 degrés de liberté).
% Il s'insert au dessus du modèle "PolyLowerTrunk_xdof" pour le bas du dos
% et permet d'accrocher un modèle de tête au dessus. Les modèles d'épaules
% sont construits à part par souci de simplicité.
% Les données anthropométriques pour ce modèle sont issues des travaux de
% Bayoglu et al. (2017a,b). La scoliose à été supprimée par alignement avec
% la vertèbre T12. Les boucles d'alignement peuvent être simplifiées.
% (Twente spine model: a complete and coherent dataset for musculo-skeletal
% modeling of the lumbar region of the human spine ;
%  Twente spine model: a complete and coherent dataset for musculo-skeletal
% modeling of the thoracic and cervical regions of the human spine)
% Auteurs : A. Schuster, G. Dumont, C. Pontonnier (2023)


list_solid={'T12_J1' 'T12_J2' 'T12'...
            'T11_J1' 'T11_J2' 'T11'...
            'T10_J1' 'T10_J2' 'T10'...
            'T9_J1' 'T9_J2' 'T9'...
            'T8_J1' 'T8_J2' 'T8'...
            'T7_J1' 'T7_J2' 'T7'...
            'T6_J1' 'T6_J2' 'T6'...
            'T5_J1' 'T5_J2' 'T5'...
            'T4_J1' 'T4_J2' 'T4'...
            'T3_J1' 'T3_J2' 'T3'...
            'T2_J1' 'T2_J2' 'T2'...
            'T1_J1' 'T1_J2' 'T1'...
            };
    
%% solid numbering incremation

s=size(OsteoArticularModel,2)+1;  %#ok<NASGU> % number of the first solid
for i=1:size(list_solid,2)      % each solid numbering: s_"nom du solide"
    if i==1
        eval(strcat('s_',list_solid{i},'=s;'))
    else
        eval(strcat('s_',list_solid{i},'=s_',list_solid{i-1},'+1;'))
    end
end  
    
% find the number of the mother from the attachment point: 'attachment_pt'
if ~numel(AttachmentPoint)
    s_mother=0;
    pos_attachment_pt=[0 0 0]';
else
    test=0;
    for i=1:numel(OsteoArticularModel)
        for j=1:size(OsteoArticularModel(i).anat_position,1)
            if strcmp(AttachmentPoint,OsteoArticularModel(i).anat_position{j,1})
               s_mother=i;
               pos_attachment_pt=OsteoArticularModel(i).anat_position{j,2}+OsteoArticularModel(s_mother).c;
               test=1;
               break
            end
        end
        if i==numel(OsteoArticularModel) && test==0
            error([AttachmentPoint ' is no existent'])        
        end       
    end
    if OsteoArticularModel(s_mother).child == 0      % if the mother don't have any child
        OsteoArticularModel(s_mother).child = eval(['s_' list_solid{1}]);    % the child of this mother is this solid
    else
        [OsteoArticularModel]=sister_actualize(OsteoArticularModel,OsteoArticularModel(s_mother).child,eval(['s_' list_solid{1}]));   % recherche de la derni�re soeur
    end
end      

%% Definition of anatomical landmarks for the segments(CoM wrt global RF ; Node wrt local RF on CoM)

%Matrice de rotation de la base du CT-Scan vers la base suivie par les recommendations de l'ISB

M = [0 -1 0 ;...
     0 0 1  ;...
    -1 0 0  ];

%Matrice de symétrie pour le changement gauche/droite

S = [1 0 0 ;...
     0 1 0 ;...
     0 0 -1];

%Position du CoM du Pelvis par rapport au CT-Scan, dans la base ISB
% CoM_Pelvis = M * (k*[-4.594051365 -31.54854965 691.377655025]'*0.001);

%Position des articulations telle que la moyenne des deux corps de
%vertèbres (sup+inf)/2 dans le repère du CT-scan:
%Calcul des longueurs des vertèbres (entre les deux corps vertébraux):

% Thoraciques %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
centre_geom_T12CT = M*k*[-0.88880004 -95.19249946 850.85706413]'*0.001;%Repère CT-Scan, on ramène tout à cette abscisse

% Alignement de la colonne

T12_sup = M*k*[-1.9339 -92.7972 862.658]'*0.001; %Centre plateau sup repère CT
T12_inf = M*k*[-0.88880004 -97.7597 839.097]'*0.001; %Centre plateau inf repère CT, abscisse barycentre T12
T11_sup = M*k*[-8.0478 -88.3005 890.497]'*0.001;
T11_inf = M*k*[-3.6646 -89.9496 867.425]'*0.001;
T10_sup = M*k*[-5.9396 -86.0169 916.308]'*0.001;
T10_inf = M*k*[-2.2081 -86.9715 894.928]'*0.001;
T9_sup = M*k*[-8.578 -87.4164 941.138]'*0.001;
T9_inf = M*k*[-5.9926 -86.5605 920.522]'*0.001;
T8_sup = M*k*[-8.4684 -88.1977 965.049]'*0.001;
T8_inf = M*k*[-8.1813 -87.8752 944.919]'*0.001;
T7_sup = M*k*[-9.8847 -90.9386 989.018]'*0.001;
T7_inf = M*k*[-8.5026 -91.1361 968.422]'*0.001;
T6_sup = M*k*[-7.9465 -101.947 1009.79]'*0.001;
T6_inf = M*k*[-9.4087 -97.3257 991.257]'*0.001;
T5_sup = M*k*[-5.3964 -109.896 1028.45]'*0.001;
T5_inf = M*k*[-7.103 -103.244 1012.33]'*0.001;
T4_sup = M*k*[-1.0067 -121.573 1045.26]'*0.001;
T4_inf = M*k*[-4.137 -112.299 1031.48]'*0.001;
T3_sup = M*k*[2.3591 -134.426 1061.02]'*0.001;
T3_inf = M*k*[-1.0567 -124.563 1048.33]'*0.001;
T2_sup = M*k*[5.4555 -148.744 1075.04]'*0.001;
T2_inf = M*k*[-1.099 -138.551 1062.85]'*0.001;
T1_sup = M*k*[11.2313 -159.726 1089.99]'*0.001;
T1_inf = M*k*[6.2343 -151.013 1078.71]'*0.001;

% Longueur de toutes les vertèbres :

T12_length = norm(T12_sup-T12_inf);  
T11_length = norm(T11_sup-T11_inf);
T10_length = norm(T10_sup-T10_inf);
T9_length = norm(T9_sup-T9_inf);
T8_length = norm(T8_sup-T8_inf);
T7_length = norm(T7_sup-T7_inf);
T6_length = norm(T6_sup-T6_inf);
T5_length = norm(T5_sup-T5_inf);
T4_length = norm(T4_sup-T4_inf);
T3_length = norm(T3_sup-T3_inf);
T2_length = norm(T2_sup-T2_inf);
T1_length = norm(T1_sup-T1_inf);

seg_length = {T12_length,T11_length,T10_length,T9_length,T8_length,...
              T7_length,T6_length,T5_length,T4_length,T3_length,T2_length,...
              T1_length};

% Longueur de tous les disques :

DI11_length = norm(T11_inf-T12_sup);%Distance entre les deux points
DI10_length = norm(T10_inf-T11_sup);%Distance entre les deux points
DI9_length = norm(T9_inf-T10_sup);%Distance entre les deux points
DI8_length = norm(T8_inf-T9_sup);%Distance entre les deux points
DI7_length = norm(T7_inf-T8_sup);%Distance entre les deux points
DI6_length = norm(T6_inf-T7_sup);%Distance entre les deux points
DI5_length = norm(T5_inf-T6_sup);%Distance entre les deux points
DI4_length = norm(T4_inf-T5_sup);%Distance entre les deux points
DI3_length = norm(T3_inf-T4_sup);%Distance entre les deux points
DI2_length = norm(T2_inf-T3_sup);%Distance entre les deux points
DI1_length = norm(T1_inf-T2_sup);%Distance entre les deux points

IVD_length = {DI11_length,DI10_length,DI9_length,DI8_length,DI7_length,...
              DI6_length,DI5_length,DI4_length,DI3_length,DI2_length,...
              DI1_length};

% On met les points dans deux listes

L1 = {T12_inf;T12_sup;T11_inf;T11_sup;T10_inf;T10_sup;T9_inf;T9_sup;T8_inf;T8_sup;...
     T7_inf;T7_sup;T6_inf;T6_sup;T5_inf;T5_sup;T4_inf;T4_sup;T3_inf;T3_sup;...
     T2_inf;T2_sup;T1_inf;T1_sup};

L2 = {T12_inf;T12_sup;T11_inf;T11_sup;T10_inf;T10_sup;T9_inf;T9_sup;T8_inf;T8_sup;...
     T7_inf;T7_sup;T6_inf;T6_sup;T5_inf;T5_sup;T4_inf;T4_sup;T3_inf;T3_sup;...
     T2_inf;T2_sup;T1_inf;T1_sup};

% On verticalise les segments
y_seg = cell(0); %Composante y des segments redressés
for i = 1:12
    I = cell2mat(L1(2*i-1));
    J = cell2mat(L1(2*i));
    J_vert = [J(1);...
              sqrt((J(2)-I(2))^2 + (J(3)-I(3))^2) + I(2);...
              I(3)];
    L1(2*i)={J_vert};
    y_seg = [y_seg ; J(2)-I(2)];
end

% On verticalise les IVD
y_ivd = cell(0); %Composante y des disques redressés
for i = 1:11
    I = cell2mat(L2(2*i));
    J = cell2mat(L2(2*i+1));
    J_vert = [J(1);...
              sqrt((J(2)-I(2))^2 + (J(3)-I(3))^2) + I(2);...
              I(3)];
    L2(2*i+1)={J_vert};
    y_ivd = [y_ivd ; J(2)-I(2)];
end

abscisse = T12_inf(3);
for i = 2:12
    pre = cell2mat(L1(2*i-2));
    inf = cell2mat(L1(2*i-1)); % le point correspondant au plateau inf
    sup = cell2mat(L1(2*i)); % le point correspondant au plateau sup
    inf(3) = abscisse;
    sup(3) = abscisse;
    y_disque = cell2mat(y_ivd(i-1));
    sup(2) = sup(2) + (pre(2) + y_disque) - inf(2);
    inf(2) = pre(2) + y_disque;
    L1(2*i-1) = {inf};
    L1(2*i) = {sup};
end

ind = 1;
for i = 0:11
    ind_impair=(2*i+1);
    ind_pair  =ind_impair+1;
    assignin('base',['T',num2str(13-ind),'_inf'],cell2mat(L1(ind_impair)))
    assignin('base',['T',num2str(13-ind),'_sup'],cell2mat(L1(ind_pair)))
    ind = ind + 1;
end

