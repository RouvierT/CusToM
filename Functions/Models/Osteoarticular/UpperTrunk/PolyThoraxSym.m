CoM_T6 = CoM_T6CT - T7_T6CT;

CoM_T5CT = centre_geom_T5CT + M*k*[0.4 -3.4 0]'*T5_length; 
CoM_T5 = CoM_T5CT - T6_T5CT;

CoM_T4CT = centre_geom_T4CT + M*k*[0.1 -2.8 0]'*T4_length;
CoM_T4 = CoM_T4CT - T5_T4CT;

CoM_T3CT = centre_geom_T3CT + M*k*[-0.2 -2.0 0]'*T3_length; 
CoM_T3 = CoM_T3CT - T4_T3CT;

CoM_T2CT = centre_geom_T2CT + M*k*[-0.4 -1.3 0]'*T2_length; 
CoM_T2 = CoM_T2CT - T3_T2CT;

CoM_T1CT = centre_geom_T1CT + M*k*[-0.5 -0.8 0]'*T1_length; 
CoM_T1 = CoM_T1CT - T2_T1CT;

%Centre de masse du thorax avec la formule du barycentre %%%%%%%%%%%%%%%%%%

CoM_ThoraxCT = (MassT1*CoM_T1CT+MassT2*CoM_T2CT+MassT3*CoM_T3CT+MassT4*CoM_T4CT+...
                MassT5*CoM_T5CT+MassT6*CoM_T6CT+MassT7*CoM_T7CT+MassT8*CoM_T8CT+...
                MassT9*CoM_T9CT+MassT10*CoM_T10CT+MassT11*CoM_T11CT+MassT12*CoM_T12CT)/MassThorax;

CoM_Thorax = CoM_ThoraxCT - L1_ThoraxCT;%Repère segment parent
Thorax_C7JointNodeCT = M*k*[13 -161.816 1093.62]'*0.001;
Thorax_C7JointNode = Thorax_C7JointNodeCT - T2_T1CT;
CervicalNode = Thorax_C7JointNode;
C1_HeadJointNodeCT = M*k*[29.6668 -195.846 1197.5]'*0.001; %Correspond au sommet de la C2
C1_HeadJointNode = C1_HeadJointNodeCT - T2_T1CT;
NeckNode = C1_HeadJointNode;

% Attachement Bras %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

Thorax_RClavJointNodeCT = M*k*[-10.7812 -205.974 1051.98]'*0.001; %Comme OS donc OK
Thorax_scjJointRightNode = Thorax_RClavJointNodeCT - T2_T1CT;
Thorax_scjJointLeftNode = S*Thorax_scjJointRightNode;
RScapula_HumerusJointNodeCT = M*k*[-120.114 -151.754 1083.89]'*0.001;
Thorax_ShoulderRightNode = RScapula_HumerusJointNodeCT - T2_T1CT; %Bras droit dans le repère T1
Thorax_ShoulderLeftNode = S*(RScapula_HumerusJointNodeCT - T2_T1CT);  %Bras gauche dans le repère T1

