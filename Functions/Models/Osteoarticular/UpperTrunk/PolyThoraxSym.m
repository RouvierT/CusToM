L1_sup = M*k*[-1.2769 -101.421 832.204]'*0.001; %Depuis PolyLowerTrunk
L1_ThoraxCT = (L1_sup+T12_inf)/2;
L1_T12CT = (L1_sup+T12_inf)/2;
%T12 à T1
T12_T11CT = (T12_sup+T11_inf)/2;%Joint nodes (global)
T12_T11JointNode = T12_T11CT-L1_T12CT;%Joint nodes (local)
T11_T10CT = (T11_sup+T10_inf)/2;%Joint nodes (global)
T11_T10JointNode = T11_T10CT-T12_T11CT;%Joint nodes (local)
T10_T9CT = (T10_sup+T9_inf)/2;%Joint nodes (global)
T10_T9JointNode = T10_T9CT-T11_T10CT;%Joint nodes (local)
T9_T8CT = (T9_sup+T8_inf)/2;%Joint nodes (global)
T9_T8JointNode = T9_T8CT-T10_T9CT;%Joint nodes (local)
T8_T7CT = (T8_sup+T7_inf)/2;%Joint nodes (global)
T8_T7JointNode = T8_T7CT-T9_T8CT;%Joint nodes (local)
T7_T6CT = (T7_sup+T6_inf)/2;%Joint nodes (global)
T7_T6JointNode = T7_T6CT-T8_T7CT;%Joint nodes (local)
T6_T5CT = (T6_sup+T5_inf)/2;%Joint nodes (global)
T6_T5JointNode = T6_T5CT-T7_T6CT;%Joint nodes (local)
T5_T4CT = (T5_sup+T4_inf)/2;%Joint nodes (global)
T5_T4JointNode = T5_T4CT-T6_T5CT;%Joint nodes (local)
T4_T3CT = (T4_sup+T3_inf)/2;%Joint nodes (global)
T4_T3JointNode = T4_T3CT-T5_T4CT;%Joint nodes (local)
T3_T2CT = (T3_sup+T2_inf)/2;%Joint nodes (global)
T3_T2JointNode = T3_T2CT-T4_T3CT;%Joint nodes (local)
T2_T1CT = (T2_sup+T1_inf)/2;%Joint nodes (global)
T2_T1JointNode = T2_T1CT-T3_T2CT;%Joint nodes (local)

%Position du CoM du segment enfant par rapport au repère associé au segment parent
% avec les paramètres de Pearsall et al. (1996) (Chaîne cinématique)
% Dans Pearsall et al. on a :
% x transverse
% y anteropostérieur
% z longitudinal mais vers le bas : on a fait le changement pour y au préalable

% Masses %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%Each vertebrae masses are computed as a pourcentage of the trunk mass :

MassThorax = 44.6/100*Mass.Trunk_Mass;
MassT1 = 2.7/100*Mass.Trunk_Mass;
MassT2 = 2.6/100*Mass.Trunk_Mass;
MassT3 = 3.3/100*Mass.Trunk_Mass;
MassT4 = 3.1/100*Mass.Trunk_Mass;
MassT5 = 3.2/100*Mass.Trunk_Mass;
MassT6 = 3.2/100*Mass.Trunk_Mass;
MassT7 = 3.4/100*Mass.Trunk_Mass;
MassT8 = 3.6/100*Mass.Trunk_Mass;
MassT9 = 3.8/100*Mass.Trunk_Mass;
MassT10 = 4.8/100*Mass.Trunk_Mass;
MassT11 = 5.0/100*Mass.Trunk_Mass;
MassT12 = 6.0/100*Mass.Trunk_Mass;

CoM_T12CT = centre_geom_T12CT + M*k*[-0.6 -4.1 0]'*T12_length;
CoM_T12 = CoM_T12CT - L1_T12CT;

% On s'occupe maintenant des centre géométriques des vertèbres
% Points :
centre_geom_T11CT = M*k*[-3.836850765 -90.205750485 879.28863221]'*0.001;%Repère CT-Scan
centre_geom_T10CT = M*k*[-4.74510698 -88.076947225 905.66991757]'*0.001;%Repère CT-Scan
centre_geom_T9CT = M*k*[-5.831965935 -87.096696205 930.94956846]'*0.001;%Repère CT-Scan
centre_geom_T8CT = M*k*[-7.09154992 -86.621049795 955.103456975]'*0.001;%Repère CT-Scan
centre_geom_T7CT = M*k*[-7.872599875 -90.458102525 978.58589888]'*0.001;%Repère CT-Scan
centre_geom_T6CT = M*k*[-7.65406297 -98.26729827 1000.734251955]'*0.001;%Repère CT-Scan
centre_geom_T5CT = M*k*[-6.150750095 -106.459300965 1020.276397465]'*0.001;%Repère CT-Scan
centre_geom_T4CT = M*k*[-1.886976905 -117.97578271 1037.791405535]'*0.001;%Repère CT-Scan
centre_geom_T3CT = M*k*[2.029917365 -130.95841247 1053.39871453]'*0.001;%Repère CT-Scan
centre_geom_T2CT = M*k*[4.85567504 -142.766151575 1069.3564713]'*0.001;%Repère CT-Scan
centre_geom_T1CT = M*k*[9.70759988 -154.560297725 1084.445774555]'*0.001;%Repère CT-Scan

geom1 = {centre_geom_T12CT;centre_geom_T11CT;centre_geom_T10CT;centre_geom_T9CT;...
        centre_geom_T8CT;centre_geom_T7CT;centre_geom_T6CT;centre_geom_T5CT;...
        centre_geom_T4CT;centre_geom_T3CT;centre_geom_T2CT;centre_geom_T1CT};

geom2 = {centre_geom_T12CT;centre_geom_T11CT;centre_geom_T10CT;centre_geom_T9CT;...
        centre_geom_T8CT;centre_geom_T7CT;centre_geom_T6CT;centre_geom_T5CT;...
        centre_geom_T4CT;centre_geom_T3CT;centre_geom_T2CT;centre_geom_T1CT};

%On pverticalise les vecteurs d'indices pairs
for i = 1:6
    I = cell2mat(geom1(2*i-1));
    J = cell2mat(geom1(2*i));
    J_vert = [J(1);...
              sqrt((J(2)-I(2))^2 + (J(3)-I(3))^2) + I(2);...
              I(3)];
    geom1(2*i)={J_vert};
end

%On verticalise les vecteurs d'indices impaires
y_geom = cell(0);
for i = 1:5
    I = cell2mat(geom2(2*i));
    J = cell2mat(geom2(2*i+1));
    J_vert = [J(1);...
              sqrt((J(2)-I(2))^2 + (J(3)-I(3))^2) + I(2);...
              I(3)];
    geom2(2*i+1)={J_vert};
    y_geom = [y_geom ; J(2)-I(2)];
end

abscisse = centre_geom_T12CT(3);
for i = 2:6
    pre = cell2mat(geom1(2*i-2));
    inf = cell2mat(geom1(2*i-1)); % le point correspondant au plateau inf
    sup = cell2mat(geom1(2*i)); % le point correspondant au plateau sup
    inf(3) = abscisse;
    sup(3) = abscisse;
    y_CG = cell2mat(y_geom(i-1));
    sup(2) = sup(2) + (pre(2) + y_CG) - inf(2);
    inf(2) = pre(2) + y_CG;
    geom1(2*i-1) = {inf};
    geom1(2*i) = {sup};
end

for ind = 1:12
    assignin('base',['centre_geom_T',num2str(13-ind),'CT'],cell2mat(geom1(ind)))
end

CoM_T11CT = centre_geom_T11CT + M*k*[-0.5 -4.4 0]'*T11_length; 
CoM_T11 = CoM_T11CT - T12_T11CT;

CoM_T10CT = centre_geom_T10CT + M*k*[-0.2 -4.6 0]'*T10_length; 
CoM_T10 = CoM_T10CT - T11_T10CT;

CoM_T9CT = centre_geom_T9CT + M*k*[-0.1 -4.6 0]'*T9_length; 
CoM_T9 = CoM_T9CT - T10_T9CT;

